window.onload = function exampleFunction() {
    let f = Math.floor(Math.random() * 4) + 1;
    if (f === 1) {
        document.querySelector("h1").innerHTML=".(-.-)."
    } else if (f === 2) {
        document.querySelector("h1").innerHTML="(>.<)"
    } else if (f === 3) {
        document.querySelector("h1").innerHTML="-(._.)-"
    } else if (f === 4) {
        document.querySelector("h1").innerHTML="(-^_^-)"
    } 
} 